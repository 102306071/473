<div id="epaper_content_words">
<br>
&nbsp; &nbsp;「獎勵人文與社會科學領域博士候選人撰寫博士論文」是科技部為培育人文與社會科學領域之研究人才，獎勵具有研究潛力之博士候選人於博士論文寫作階段，專注於博士論文之撰寫，以提升論文品質與學術研究水準，本校105年度共有14位博士生表現突出,其中財務管理研究所盧建霖、台灣史研究所莊勝全、民族研究所陳胤安、英國語文學研究所張建明及新聞研究所盧安邦等5位獲獎生於論壇中精彩經驗分享。
<br>
&nbsp; &nbsp;科技部每年於二月至三月期間開放博士候選人撰寫博士論文申請，獎勵期間自當年度八月一日起至次年七月三十一日止，獲獎者每人每月新臺幣36,000元獎勵金，共計新台幣432,000元整獎勵金。
<br>
&nbsp; &nbsp;為幫助有意申請的博士生掌握申請及執行秘訣，研究發展處特別舉辦經驗分享會，邀請獲獎者到場分享成功申請的心得及事前準備功夫，歡迎有興趣的師生踴躍報名參加。
