<div class="epaper_content_head"><a name="r1">CEO論壇：征服法國 面膜商李昆霖以感動創造品牌</a>
<div align="right" style="padding-right:15px">校訊記者劉玟均報導</div>
</div>
<div id="content">
<br>
&nbsp; &nbsp;探討行銷與品牌經營實務，企管系CEO論壇2日邀請Timeless Truth Mask執行長李昆霖分享經營面膜品牌與進軍法國的心得。他善用社群行銷，拉近忠實顧客和產品的距離，進而打造特色，也鼓勵同學們從經營個人品牌開始嘗試練習品牌經營。
<br>
&nbsp; &nbsp;李昆霖坦言，自己的品牌經營之路一開始並不順遂。旗下面膜最早在電視購物頻道經營，也嘗試進入百貨公司臨時櫃、藥妝通路、陸客店等通路銷售，然而並沒有得到廣大的迴響。參加世界各地美容保養展覽尋求願意投資的代理商，但沒有遇到願意賞識的伯樂。
<br>
&nbsp; &nbsp;李昆霖指出，為了節省成本，他們減少工廠建設及國際美容保養展覽攤位裝潢，然而這樣的做法無法吸引代理商的目光，也無法得到被投資的機會。後來他明白，想要塑造成功的品牌，必須先勇於投資自己，才可能得到他人的投資。經過多方的嘗試及挫折，終於漸漸在國外市場站穩腳步，打造出屬於自己的特色。
<br>
&nbsp; &nbsp;除了增加投資，李昆霖認為，自家面膜品牌成功的另一個關鍵是「社群行銷」。不只花許多時間經營與社群網站上粉絲的關係，每到一個國家參展時，李昆霖還會親手寫明信片給許多粉絲；或和粉絲一起邊敷面膜邊看棒球賽，增進彼此的互動與情感連結，「人跟感動永遠是最重要的。」
<br>
&nbsp; &nbsp;他說，得到廣大粉絲群的支持後，忠實顧客會幫忙宣揚產品，許多宣傳成本也能下降，有些人甚至會錄製有趣的敷面膜影片，增加品牌的曝光度。粉絲陪伴著品牌的成長過程，甚至賦予了品牌個性。因此李昆霖深信，品牌是有靈魂的，同時明白成功不是靠才能，而是靠準備，無論經營公司或是客群，都需要花時間及心力。
<br>
&nbsp; &nbsp;對於在場學生，李昆霖鼓勵大家要勇於表達自己，走入社群行銷前，他自己當了十幾年的部落客，累積不少粉絲。他建議同學從現在開始經營自己的個人品牌，寫部落格、經營臉書都是很好的方式。
<br>
&nbsp; &nbsp;教育一陳浩對李昆霖的做事態度及衝勁印象深刻；金融四林映汝則表示，自己很佩服李昆霖對於品牌經營的用心程度，對照過去所學行銷相關課程，對照李昆霖的故事，更增添許多真實感，印證所學。<br><br>

